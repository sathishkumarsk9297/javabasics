package com.basic;

public class switchloop {
public static void main(String[] args)
{
	int count=20;
	switch (count)
	{
	case 10:
		System.out.println(10);
		break;
	case 20:
		System.out.println(20);
		break;
	case 30:
		System.out.println(30);
		break;
	default:
		System.out.println("default");
		break;
	}
}
}
